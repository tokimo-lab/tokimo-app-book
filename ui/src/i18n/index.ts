export function isZhLocale(locale: string | null | undefined): boolean {
  return locale?.toLowerCase().startsWith("zh") ?? false;
}

function formatTemplate(
  template: string,
  vars?: Record<string, string | number>,
): string {
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (match, key: string) => {
    const value = vars[key];
    return value === undefined ? match : String(value);
  });
}

export function getBookI18n(locale: string | null | undefined): {
  t: BookTranslator;
  uiLocale: typeof uiZhCN;
} {
  const dict = isZhLocale(locale) ? zhCN : enUS;
  return {
    t: (key, vars) => formatTemplate(dict[key], vars),
    uiLocale: isZhLocale(locale) ? uiZhCN : uiEnUS,
  };
}

export function useBookI18n(): {
  locale: string | null | undefined;
  t: BookTranslator;
  uiLocale: typeof uiZhCN;
} {
  const ctx = useAppCtx();
  return useMemo(
    () => ({ locale: ctx.locale, ...getBookI18n(ctx.locale) }),
    [ctx.locale],
  );
}
